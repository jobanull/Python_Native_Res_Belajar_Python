# Upper
kata = 'dicoding'
kata = kata.upper()
print(kata)

"""
Output:
DICODING
"""

# Lower
kata = 'DICODING'
kata = kata.lower()
print(kata)

"""
Output:
dicoding
"""

#rstrip
print("Dicoding          ".rstrip())

"""
Output:
Dicoding
"""

#lstrip
print("           Dicoding".lstrip())

"""
Output:
Dicoding
"""

#strip
print("         Dicoding          ".strip())

"""
Output:
Dicoding
"""